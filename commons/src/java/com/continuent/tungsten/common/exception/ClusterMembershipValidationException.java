package com.continuent.tungsten.common.exception;

public class ClusterMembershipValidationException extends Exception
{

    public ClusterMembershipValidationException()
    {
       
    }

    public ClusterMembershipValidationException(String arg0)
    {
        super(arg0);
       
    }

    public ClusterMembershipValidationException(Throwable arg0)
    {
        super(arg0);
       
    }

    public ClusterMembershipValidationException(String arg0, Throwable arg1)
    {
        super(arg0, arg1);
       
    }

}
